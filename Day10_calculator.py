def add(x, y):
    return x + y


def subtract(x, y):
    return x - y


def multiply(x, y):
    return x * y


def divide(x, y):
    return x / y


calculator = {"+": add, "-": subtract, "*": multiply, "/": divide}


def calculate():
    print(logo)
    num1 = float(input("Please enter the first number: "))
    for key in calculator:
        print(key)

    should_continue = True
    while should_continue:
        operation = input("Please choose an operation to perform: ")
        num2 = float(input("Please enter the second number: "))
        function = calculator[operation]
        result1 = function(num1, num2)
        print(f"{num1} {operation} {num2} = {result1}")

        again = input("Would you like to perform another operation? (y/n): ").lower()
        if again == "y":
            num1 = result1
        else:
            should_continue = False
            calculate()


calculate()
